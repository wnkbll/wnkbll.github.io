        Немет). Они осознавали неизбежность краха старой модели и сознательно пошли на диалог
        с обществом ради сохранения стабильности.
      </li>
      <li>
        <strong>Юридическая легитимность оппозиции.</strong> Венгерская оппозиция (Венгерский
        демократический форум, Альянс свободных демократов) сделала ставку не на уличные баррикады,
        а на глубокую правовую экспертизу. Изменения вносились через действующий парламент,
        что исключило правовой вакуум.
      </li>
      <li>
        <strong>«Доктрина Синатры» со стороны СССР.</strong> Михаил Горбачев официально отказался
