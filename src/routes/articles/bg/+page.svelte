    <h2>Ключевые факторы болгарского сценария</h2>
    <p>
      Болгарская «Осень народов» обошлась без массового кровопролития, несмотря на
      острые политические и межнациональные противоречия. Успех мирного транзита
      определили следующие факторы:
    </p>

    <ul class="factors-list">
      <li>
        <strong>Инициатива «сверху» и раскол элит.</strong> Перемены начались не с уличного
        штурма, а с прагматичного решения части партийной номенклатуры устранить консервативного
        лидера. Это позволило избежать силового противостояния на начальном, самом опасном
        этапе.
      </li>
      <li>
        <strong>Институционализация оппозиции.</strong> Быстрое создание СДС и начало работы
        Национального круглого стола перевели конфликт с улиц в русло легального политического
        диалога, по примеру Польши и Венгрии.
      </li>
      <li>
        <strong>Оперативное разрешение этнического кризиса.</strong> В конце декабря 1989
        года новое руководство страны официально осудило «Возродительный процесс» и вернуло
        болгарским туркам их гражданские права, имена и свободу вероисповедания. Это мгновенно
        сняло напряженность и предотвратило гражданский конфликт по сценарию Югославии.
      </li>
    </ul>

    <blockquote>
      <p>
        <strong>Главный итог:</strong> Болгария успешно прошла первую фазу демократического
        транзита. Хотя на первых свободных выборах 1990 года победу одержали реформированные
        коммунисты (уникальный случай для стран Восточной Европы), монополия на власть была
        разрушена навсегда, а в августе 1990 года президентом страны стал лидер оппозиции
        Желю Желев.
